package Agenda;

public class ContatoClass {
	private String nome;
	private String telefone;
	
	public ContatoClass(String nome, String telefone) {
		this.nome = nome;
		this.telefone = telefone;
	}
	
	public String getNome() {
		return nome;
	}
	
	public String getTelefone() {
		return telefone;
	}
	
}
